// Renderer
function drawSprite(ctx, frame, x, y, scale, color) {
  ctx.fillStyle = color;
  for (let r = 0; r < frame.length; r++)
    for (let c = 0; c < frame[r].length; c++)
      if (frame[r][c]) ctx.fillRect(x + c * scale, y + r * scale, scale, scale);
}
